<h2><a name="presentation">Présentation</a></h2>
    <p>Visual Paradigm est un modélisateur d'UML visuel et outil de cas facile à utiliser, puissant et multi-platforme. Visual Paradigm fournit aux développeurs de logiciels la plate-forme de développement d'avant-garde pour bâtir des applications de qualité plus rapidement, mieux et pour moins cher!</p>

<h3><a name="prealables">Préalables</a></h3>
    <ul>
		<li>Intel Pentium 4 à 2.0 GHz ou plus récent</li>
		<li>Minimum 2.0 GB RAM, mais 4.0 GB est recommandé.</li>
		<li>Minimum 4GB d'espace sur le disque (N'INCLUE PAS la taille des projets).</li>
		<li>Microsoft Windows (XP/Vista/7/8), Microsoft Windows Server (2000/2003/2008/2012), Linux, Mac OS X</li>
	</ul>
	
<h2><a name="windows">Windows</a></h2>

<h3><a name="windows-telechargement">Téléchargement</a></h3>
    <p>Des liens de téléchargement peuvent être trouvés sur <a href="http://www.visual-paradigm.com/download/">http://www.visual-paradigm.com/download/</a>.</p>
    <p>Visual Paradigm 12.1 édition communauté (9 mai 2015):</p>
	<ul>
