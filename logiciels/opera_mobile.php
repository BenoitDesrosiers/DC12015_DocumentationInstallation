<h2><a name="presentation">Présentation</a></h2>
    <p>Opera Mobile Emulator est un émulateur de téléphones intelligents et de tablettes qui facilite le développement d'application mobile.</p>

<h2><a name="windows">Windows</a></h2>

<h3><a name="windows-prealables">Préalables</a></h3>
    <p>L'installation de GIMP nécessite Windows XP, Windows Vista, Windows 7 ou Windows 8.</p>

<h3><a name="windows-telechargement">Téléchargement</a></h3>
    <p>Des liens de téléchargement peuvent être trouvés sur <a href="http://www.opera.com/developer/mobile-emulator">le site d'Opera</a>.</p>

<h3><a name="windows-installation">Procédure d'installation</a></h3>
    <p>Lancer l'exécutable. Ensuite, cliqué sur <i>Next</i>.</p>
    <img alt="Lancement de l'installation" src="../ressources/images/opera/opera-1.png"/>
    <p>Sélectionnez le dossier où vous voulez installer le logiciel, puis cliquez sur <i>Next</i>.</p>
    <img alt="Destination de l'installation du logiciel" src="../ressources/images/opera/opera-2.png"/>
    <p>Sélectionnez le dossier où vous voulez installer le raccourci du logiciel, puis cliquez sur <i>Next</i>.</p>
    <img alt="Destination du raccourci du logiciel" src="../ressources/images/opera/opera-3.png"/>
    <p>Sélectionnez les icones additionnels que vous voulez créer, puis cliquez sur <i>Next</i>.</p>
    <img alt="Choix des icones." src="../ressources/images/opera/opera-4.png"/>
    <p>Cliquez sur <i>Install</i> pour lancer l'installation.</p>
	<img alt="Résumé des choix précédents" src="../ressources/images/opera/opera-5.png"/>
    <p>Attendre que l'installation se termine.</p>
    <img alt="Installation en cours" src="../ressources/images/opera/opera-6.png"/>
    <p>L'installation est terminée. Si vous ne désirez pas lancer le logiciel tout de suite, décochez la case <i>Launch Opera Mobile Emulator</i>. Cliquez sur <i>Finish</i> pour quitter l'installateur.</p>
    <img alt="Création de raccourcis" src="../ressources/images/opera/opera-7.png"/>


