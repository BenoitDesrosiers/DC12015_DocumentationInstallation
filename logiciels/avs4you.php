<h2><a name="presentation">Présentation</a></h2>
    <p>Suite de logiciels payante permettant de créer et de modifier des images, des vidéos ainsi que des fichiers audios. Pendant les cours, nous nous concentrerons sur celui-ci: AvsVideo Editor</p>

<h2><a name="windows">Windows</a></h2>

	<h3><a name="windows-prealables">Préalables</a></h3>
		<p>L'installation de AVS Video Editor nécessite Windows XP ou plus récent (Vista, 7 et 8).</p>
		<p>Le logiciel demande minimalement un processeur AMD ou Intel Core 2 Duo de 3GHZ ou plus, 2go de RAM ainsi qu'une carte son compatible windows.</p>

	<h3><a name="windows-telechargement">Téléchargement</a></h3>
		<p>Des liens de téléchargement peuvent être trouvés sur <a href="http://www.avs4you.com/fr/AVS-Video-Editor.aspx">cette page</a>.</p>
		<p>En date du 17 Mars 2015, la version la plus récente est la 7.1.1.259, disponible directement <a href="http://download.avs4you.com/distributives/AVSVideoEditor.exe">ici</a>. Il est nécessaire d'avoir au moins 146.77Mo d'espace libre sur son disque dur pour pouvoir compléter le téléchargement.</p>

	<h3><a name="windows-installation">Procédure d'installation</a></h3>
		<p>Lancer l'exécutable. Une fenêtre apparaîtra, offrant de sélectionner<span style="color:red">???</span>langue utilisée par l'installateur. Sélectionner Français pour la langue.</p>
		<img alt="Sélection de la langue" src="../ressources/images/avs/etape_0.png"/>
		<p>Une fois la langue sélectionnée, choisir <i>Suivant</i> pour continuer l'installation </p>
		<img alt="Installation standard ou personnalisée" src="../ressources/images/avs/etape_1.png"/>
		<p>Prendre connaissance de la license, puis cliquer sur <i>Suivant</i>.</p>
		<img alt="Accord de license" src="../ressources/images/avs/etape_2.png"/>
		<p>Choisir un répertoire pour l'installation à l'aide du bouton <i>parcourir</i>. Laisser par défaut si aucun changement n'est nécessaire, puis cliquer sur <i>Suivant</i>.</p>
		<img alt="Choix du répertoire" src="../ressources/images/avs/etape_3.png"/>
		<p>Sélectionner la case <i>Créer l'icône du bureau pour AVS Video Editor</i> si nécessaire, laisser la case <i>Installer un échantillon de projet</i> sélectionnée, puis cliquer sur <i>Suivant</i>.</p>
		<img alt="Options d'installation" src="../ressources/images/avs/etape_4.png"/>
		<p>Prendre connaissance des informations montrées à l'écran, puis confirmer à l'aide du bouton <i>Suivant</i>.</p>
		<img alt="Confirmation d'installation" src="../ressources/images/avs/etape_5.png"/>
		<p>Patienter pendant l'installation du logiciel.</p>
		<img alt="Installation du logiciel" src="../ressources/images/avs/etape_6.png"/>
		<p>L'installation est complétée, sélectionner la case <i>Lancer AVS Video Editor</i> si nécessaire, puis cliquer sur <i>Terminer</i>.</p>
		<img alt="Terminee" src="../ressources/images/avs/etape_7.png"/>
		
